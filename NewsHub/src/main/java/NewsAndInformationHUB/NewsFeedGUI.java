    public NewsFeedGUI(NewsFeed newsFeed) {
        this();
        this.newsFeed = newsFeed;
        
        SwingUtilities.invokeLater(new Runnable() {
        public void run() {
          //setupTabs();  // Sets up the tabs for articles, videos, and research
            refreshContent();  // Refreshes the content based on the current newsFeed
        }
    });
        

        // Initialize the main tabbed pane
        mainFrame = new JTabbedPane();

        // Initialize tabbed panes for each category
        articleListTab = new JTabbedPane();
        videoListTab = new JTabbedPane();
        researchListTab = new JTabbedPane();

        // Initialize and set up the articles list inside its tabbed pane
        articleList = new JList<>();
        JScrollPane articleScrollPane = new JScrollPane(articleList);
        mainFrame.addTab("Articles", articleScrollPane);

        // Initialize and set up the videos list inside its tabbed pane
        videoList = new JList<>();
        JScrollPane videoScrollPane = new JScrollPane(videoList);
        mainFrame.addTab("Videos", videoScrollPane);

        // Initialize and set up the research list inside its tabbed pane
        researchList = new JList<>();
        JScrollPane researchScrollPane = new JScrollPane(researchList);
        mainFrame.addTab("Research", researchScrollPane);

        // Add each tabbed pane to the main frame
        mainFrame.addTab("ArticleList", articleListTab);
        mainFrame.addTab("VideoList", videoListTab);
        mainFrame.addTab("ResearchList", researchListTab);

        // Add the main frame to the panel
        setLayout(new BorderLayout());
        add(mainFrame, BorderLayout.CENTER);
        refreshContent();
    }

    public void displayArticles(List<String> articles) {
        System.out.println("Displaying articles: " + articles);
        articleList.setListData(articles.toArray(new String[0]));
    }

    public void displayVideos(List<String> videos) {
        System.out.println("Displaying videos: " + videos);
        videoList.setListData(videos.toArray(new String[0]));
    }

    public void displayResearch(List<String> research) {
        System.out.println("Displaying research: " + research);
        researchList.setListData(research.toArray(new String[0]));
    }

    public void refreshContent() {
        System.out.println("Refreshing content");
    List<String> articles = newsFeed.getLatestArticles();
    System.out.println("Articles to display: " + articles);  // Debug statement
    displayArticles(articles);

    List<String> videos = newsFeed.getLatestVideos();
    System.out.println("Videos to display: " + videos);  // Debug statement
    displayVideos(videos);

    List<String> research = newsFeed.getLatestResearch();
    System.out.println("Research to display: " + research);  // Debug statement
    displayResearch(research);
    }
    
